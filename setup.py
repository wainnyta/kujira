def install_dependencies():
    """Install Python dependencies"""
    print("\nðŸ“¦ INSTALLING DEPENDENCIES")
    print("-" * 30)
    
    try:
        # Check if virtual environment exists
        venv_path = Path('venv')
        if not venv_path.exists():
            print("Creating virtual environment...")
            subprocess.run([sys.executable, '-m', 'venv', 'venv'], check=True)
        
        # Activate virtual environment and install dependencies
        if os.name == 'nt':  # Windows
            pip_path = venv_path / 'Scripts' / 'pip'
        else:  # Unix/Linux
            pip_path = venv_path / 'bin' / 'pip'
        
        print("Installing dependencies...")
        subprocess.run([str(pip_path), 'install', '-r', 'requirements.txt'], check=True)
        print("âœ… Dependencies installed successfully")
        
    except subprocess.CalledProcessError as e:
        print(f"âŒ Error installing dependencies: {e}")
        return False
    
    return True

def initialize_database():
    """Initialize the database"""
    print("\nðŸ—„ï¸  INITIALIZING DATABASE")
    print("-" * 30)
    
    try:
        # Import and initialize database
        sys.path.insert(0, 'src')
        from src.models.trading import db
        from src.models.user import db as user_db
        from flask import Flask
        
        app = Flask(__name__)
        app.config['SQLALCHEMY_DATABASE_URI'] = 'sqlite:///src/database/app.db'
        app.config['SQLALCHEMY_TRACK_MODIFICATIONS'] = False
        
        db.init_app(app)
        user_db.init_app(app)
        
        with app.app_context():
            db.create_all()
            user_db.create_all()
        
        print("âœ… Database initialized successfully")
        return True
        
    except Exception as e:
        print(f"âŒ Error initializing database: {e}")
        return False

def create_startup_script():
    """Create startup script"""
    print("\nðŸš€ CREATING STARTUP SCRIPT")
    print("-" * 30)
    
    if os.name == 'nt':  # Windows
        script_content = """@echo off
echo Starting Cryptocurrency Trading Bot...
cd /d "%~dp0"
call venv\\Scripts\\activate
python src\\main.py
pause
"""
        script_name = 'start_bot.bat'
    else:  # Unix/Linux
        script_content = """#!/bin/bash
echo "Starting Cryptocurrency Trading Bot..."
cd "$(dirname "$0")"
source venv/bin/activate
python src/main.py
"""
        script_name = 'start_bot.sh'
    
    with open(script_name, 'w') as f:
        f.write(script_content)
    
    if os.name != 'nt':
        os.chmod(script_name, 0o755)
    
    print(f"âœ… Startup script created: {script_name}")

